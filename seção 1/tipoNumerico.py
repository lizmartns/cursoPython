# tipo float, real, decimal

#separador -> ponto
valor = 1.44
print(valor)
print(type(valor))

#vírgula indica uma tupla
valor = 1,44
print(valor)
print(type(valor))

valor1, valor2 = 1, 44
print(valor1)
print(type(valor1))
print(valor2)
print(type(valor2))

#converter um float para um int
valor =1.44
res = int(valor)
print(res)
print(type(res))
# ao converter valores float para inteiros perdemos a precisão

